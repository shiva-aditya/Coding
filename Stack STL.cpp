#include<bits/stdc++.h>
using namespace std;


int main() {
    stack<int>st;
    st.empty();
    //gives true if empty else false
    st.size();
    //gives size of stack (no of elements)
    st.top();
    //gives element at top of stack
    st.push(1);
    //push element to top of stack
    st.pop();
    //pop the top element
}
